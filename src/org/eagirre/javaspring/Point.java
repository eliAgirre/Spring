package org.eagirre.javaspring;

public class Point {

	// Attributes of class
	private int x;
	private int y;
	
	// Getters
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	// Setters
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
}